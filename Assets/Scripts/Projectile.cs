using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Projectile : MonoBehaviour
{
    private Movement2D movement2D;
    private Transform target;
    private float damage;

    private Rigidbody myRigidbody;
    //private float speed = 1.0f;
    private void OnEnable()
    {
        myRigidbody = GetComponent<Rigidbody>();
        target = GameObject.FindGameObjectWithTag("Enemy").transform;
        StartCoroutine(LoseTarget());
    }


    public void Setup(Transform target, float damage)             // 타워가 발사체를 생성할 때 호출
    {
        movement2D = GetComponent<Movement2D>();
        this.target = target;                       // 타워가 설정해준 target
        this.damage = damage;                       // 타워가 설정해준 공격력
    }

    private void Update()
    {
        if(target != null)      // target이 존재하면
        {
            // 발사체를 target의 위치로 이동
            Vector3 direction = (target.position - transform.position).normalized;
            movement2D.MoveTo(direction);

            //Vector3 direction = target.position - transform.position;
            //myRigidbody.velocity = direction.normalized * speed;
        }

        else
        {
            // 발사체 오브젝트 삭제
            Destroy(gameObject);
        }

    }

    IEnumerator LoseTarget()
    {
        yield return new WaitForSeconds(2f);
        target = null;
    }

    private void OnTriggerEnter2D(Collider2D collision)
    {
        if (!collision.CompareTag("Enemy")) return;             // 적이 아닌 대상과 부딪히면
        if (collision.transform != target) return;              // 현재 target인 적이 아닐 때

        //collision.GetComponent<Enemy>().OnDie();              // 적 사망 함수 호출
        collision.GetComponent<EnemyHP>().TakeDamage(damage);   // 적 체력을 damage만큼 감소
        Destroy(gameObject);                                    // 발사체 오브젝트 삭제
    }

}


/*
 * File : ProjectTileAttack.cs
 * Desc
 *  : 타워가 발사하는 기본 발사체에 부착
 *  
 * Functions
 *  : Update() - 타겟이 존재하면 타겟 방향으로 이동하고, 타겟이 존재하지 않으면 발사체 삭제
 *  : OnTriggerEnter2D() - 타겟으로 설정한 적과 부딪혔을 때 둘 다 삭제
 */